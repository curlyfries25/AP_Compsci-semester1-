/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        String name;
        name = new String("lebron");
        
        int number;
        number = 47;
        
        
        double money;
        money = 78.47;
        
        System.out.println(name + " lives in Beverly Hills");
        System.out.println(name + " has " + number + " bathrooms in his house");
        System.out.println(name + " is still $" + money + " in debt");
	}
}